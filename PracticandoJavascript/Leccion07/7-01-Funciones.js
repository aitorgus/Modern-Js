/*Funciones en Javascript */

//Declaracion de la función
function miFuncion(a, b) {
    console.log("Suma :" + (a + b));

}

//Aquí hacemos una llamada a la función
resultado = miFuncion(2, 3);


/* Podemos llamar a la función antes o después de declararla, este concepto se conoce como Hoisting */

