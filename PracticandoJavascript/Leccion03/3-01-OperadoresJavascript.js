//SUMA
let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la operación :", z);

//RESTA
z = a - b;
console.log("Resultado de la operación :", z);

//MULTIPLICACIÓN

z = a * b;
console.log("Resultado de la operación :", z);

//EXPONENTE
z = a ** b;
console.log("Resultado de la operación :", z);

//DIVISIÓN 
z = a / b;
console.log("Resultado de la operación :", z);

//OPERACIÓN DE MÓDULO
z = a % b;
console.log("Resultado de la operación :", z);



